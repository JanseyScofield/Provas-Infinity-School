# Você está criando um programa para calcular a área de um retângulo. O programa deve solicitar ao usuário que forneça a base e a altura do retângulo. 
# Em seguida, o programa deve calcular a área usando a fórmula:
# Área=Base×Altura

base  = float(input("Digite o tamanho da base do retângulo em metros: "))
altura  = float(input("Digite o tamanho da altura do retângulo em metros: "))

print(f"A área do retângulo é de {base*altura} m².")
